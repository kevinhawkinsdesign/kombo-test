const DICTS: Record<Locale, Dict> = { en, es, it, fr, de, pt, pt_BR }

interface LocaleState {
  locale: Locale
  setLocale: (l: Locale) => void
  t: (key: string) => string
}

const LocaleContext = React.createContext<LocaleState | undefined>(undefined)

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = React.useState<Locale>(() => {
    const saved = localStorage.getItem("kb_locale")
    return LOCALES.includes(saved as Locale) ? (saved as Locale) : "en"
  })

  // Set during render (before children) so lib/format helpers emit the new
  // language on the same paint as the COPY strings around them.
  setFormatLocale(locale, INTL_LOCALE[locale])

  const setLocale = React.useCallback((l: Locale) => {
    setLocaleState(l)
    localStorage.setItem("kb_locale", l)
  }, [])

  const t = React.useCallback(
    (key: string) => DICTS[locale][key] ?? en[key] ?? key,
    [locale]
  )

  const value = React.useMemo(
    () => ({ locale, setLocale, t }),
    [locale, setLocale, t]
  )

  return (
    <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>
  )
}

export function useLocale() {
  const ctx = React.useContext(LocaleContext)
  if (!ctx) throw new Error("useLocale must be used within a LocaleProvider")
  return ctx
}
